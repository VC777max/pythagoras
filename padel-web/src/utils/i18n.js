export const getLanguage = () => {
  const saved = localStorage.getItem("padel_pref_language");
  if (saved && (saved === "en" || saved === "nl")) return saved;
  const sysLang = navigator.language || navigator.userLanguage || "en";
  return sysLang.startsWith("nl") ? "nl" : "en";
};

export const setLanguage = (lang) => {
  if (lang === "en" || lang === "nl") {
    localStorage.setItem("padel_pref_language", lang);
  }
};

export const translate = (key, lang = "en", replacements = {}) => {
  const dict = translations[lang] || translations["en"];
  let text = dict[key] || translations["en"][key] || key;
  Object.keys(replacements).forEach(k => {
    text = text.replace(`{${k}}`, replacements[k]);
  });
  return text;
};
